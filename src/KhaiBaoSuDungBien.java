public class KhaiBaoSuDungBien {
    public static void main(String[] args) {
        int i = 10; // khai báo biến i có giá trị int là 10
        float f = 20.5f; // khai báo biến f có kiểu float là 20.5
        boolean b = true; // khai báo biến b có kiểu dữ liệu là boolean giá trị true
        char c = 'a'; // khai báo biến c có kiểu là char có giá trị là kí tự a
        String s = "Hà Nội"; // khai báo biến s có kiểu dữ liệu là string có giá trị là chuỗi Hà Nội
        System.out.println("i ="+i);
        System.out.println("f ="+f);
        System.out.println("b ="+b);
        System.out.println("c ="+c);
        System.out.println("s ="+s);
    }
}
